    public void refresh(){
        System.out.print("\nRefreshing data user page...");
        javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) dataTable.getModel();
        ArrayList<String[]> dataUser = ServerAPI.get_data_user();
        int rowCount = dataTable.getRowCount();
        for(int i=0; i<rowCount; ++i){
            tableModel.removeRow(0);
        }
        for(String[] row : dataUser){
            tableModel.addRow(row);
        }
    }
